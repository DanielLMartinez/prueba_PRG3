    private void imprimir(){
        String listadoPerro = "";
        for(Perro p : this.listaPerro){
            listadoPerro += "\n";
            listadoPerro += p;
            listadoPerro += "\n";
        };
        insert_texto.setText(listadoPerro);
    }

