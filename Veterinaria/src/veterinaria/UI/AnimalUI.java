    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        String animal = insert_animal.getSelectedItem().toString();
        String nombre = insert_nombre.getText();
        String strchip = insert_chip.getText();
        int chip = 0;
        String color = insert_color.getSelectedItem().toString();
        boolean castrado = insert_castrado.isSelected();
        
        
        try{
            chip = Integer.parseInt(strchip);
            }catch(Exception ex){
                HayErrores = true;
            }
        if(!HayErrores){
            if(insert_animal.getSelectedItem().toString()=="Perro"){
                
                Perro p = new Perro();
                p.setNombre(nombre);
                p.setChip(chip);
                p.setColor(color);
                p.setCastrado(castrado);
                this.listaPerro.add(p);
                
                imprimir();
                JOptionPane.showMessageDialog(this, "Perrito ingresado", "Dialog", JOptionPane.ERROR_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(this, "Hay errores en el formulario. Favor verifique antes de enviar.", "Dialog",
            JOptionPane.ERROR_MESSAGE);
        }
                
                
                
                
                
             
            }
                
                
    }//GEN-LAST:event_guardarActionPerformed

